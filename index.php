<h2>How does the virus spread?</h2>
The virus that causess Covid-19 is mainly transmitted through 
droplets generated when  and infected person cough, sneeze, or 
exhale. 
<br> These droplets are too heavy to hang in the air, and quickly fall 
on floor or other surface 

